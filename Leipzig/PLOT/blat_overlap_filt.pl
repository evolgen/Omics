	@en=split /\t/,$array[$r];

if($e[0] eq $en[0] && $e[2] ge $en[1] && $e[6] eq $en[6]) {
	print OUT "$array[$k]\n$array[$r]\n";
	$r++;$k++;
}

elsif($en[1]>=$e[1] &&  $en[2]<=$e[2] && $en[6]<=$e[6]) {
	$r++;
}

else {
$k++;$r++;
}

}


